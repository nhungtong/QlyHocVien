    private void loadDuLieuVaoBang() {
    try {
        KetNoi kn = new KetNoi();
        ResultSet rs = kn.GetData("hocvien");
        DefaultTableModel model = (DefaultTableModel) tblhocvien.getModel();
        model.setRowCount(0); 
        while (rs.next()) {
            Object[] rowData = { rs.getString("maHv"), rs.getString("tenHv"), rs.getString("queQuan"), rs.getString("diemThi") };
            model.addRow(rowData);
        }
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(Frm_qlhv.class.getName()).log(Level.SEVERE, "Lỗi truy xuất dữ liệu", ex);
    }
}
