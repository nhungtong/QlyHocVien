    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        try {
            KetNoi kn = new KetNoi();
            String maHv = txtmahv.getText();
            String tenHv = txthoten.getText();
            String queQuan = txtquequan.getText();
            float diemThi = 0.0f;
            try {
                diemThi = Float.parseFloat(txtdiemthi.getText());
            } catch (NumberFormatException ex) {
                throw new IllegalArgumentException("Điểm thi không hợp lệ. Vui lòng nhập số.");
            }
            if (maHv.isEmpty() || tenHv.isEmpty() || queQuan.isEmpty()) {
                throw new IllegalArgumentException("Vui lòng điền đầy đủ thông tin.");
            }
            kn.Themhv(maHv, tenHv, queQuan, diemThi);
            loadDuLieuVaoBang();
            kn.Close();
        } catch (IllegalArgumentException ex) {
            Logger.getLogger(Frm_qlhv.class.getName()).log(Level.WARNING, "Lỗi xác nhận: ", ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Frm_qlhv.class.getName()).log(Level.SEVERE, "Không tìm thấy:", ex);
        } catch (SQLException ex) {
            Logger.getLogger(Frm_qlhv.class.getName()).log(Level.SEVERE, "Xảy ra ngoại lệ SQL:", ex);
        }
    }//GEN-LAST:event_btnthemActionPerformed

